    private void samadenganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_samadenganActionPerformed
        // TODO add your handling code here:
        switch(pilihan){
            
            case 1:
                bilangandua=Double.parseDouble(angka);
                total = bilangansatu/bilangandua;
                angka = Double.toString(total);
                layar.setText(angka);
                break;
            case 2:
                bilangandua=Double.parseDouble(angka);
                total = bilangansatu+bilangandua;
                angka = Double.toString(total);
                layar.setText(angka);
                break;
            case 3:
                bilangandua=Double.parseDouble(angka);
                total = bilangansatu-bilangandua;
                angka = Double.toString(total);
                layar.setText(angka);
                break;
            case 4:
                bilangandua=Double.parseDouble(angka);
                total = bilangansatu*bilangandua;
                angka = Double.toString(total);
                layar.setText(angka);
                break;
            case 5:
            bilangandua=Double.parseDouble(angka);
                total = Math.pow(bilangansatu,bilangandua);
                angka = Double.toString(total);
                layar.setText(angka);
                break;
            case 6:
                total = bilangansatu/100;
                angka = Double.toString(total);
                layar.setText(angka);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_samadenganActionPerformed

